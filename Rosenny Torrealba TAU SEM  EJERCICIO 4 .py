# 1. Creamos la lista de números del 1 al 10
# range(1, 11) genera números del 1 al 10 (el límite superior es exclusivo)
numeros = list(range(1, 11))

# 2. Calculamos la suma de todos los elementos de la lista
resultado_suma = sum(numeros)

# Mostramos los resultados
print(f"Lista de números: {numeros}")
print(f"La suma total es: {resultado_suma}")